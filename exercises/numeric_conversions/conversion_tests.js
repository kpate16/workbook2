var a = " 101.1 ";
var b = "55";
var c = "402 Stevens";
var d = "Number 5 ";

//ParseInt Method
var a1 = parseInt(a);
console.log(a1);

var b1 = parseInt(b);
console.log(b1);

var c1 = parseInt(c);
console.log(c1);

var d1 = parseInt(d);
console.log(d1);

//Parse Float Method
var a1 = parseFloat(a);
console.log(a1);

var b1 = parseFloat(b);
console.log(b1);

var c1 = parseFloat(c);
console.log(c1);

var d1 = parseFloat(d);
console.log(d1);

